package aula2;

public class TiposPrimitivos {

	public static void main(String[] args) {
		// Exemplos de tipos primitivos em Java
		
		char sexo = 'f';  //tipo primitivo char
		
		byte idade = 89;   // tipo primitivo byte
		
		short codigo = 256 ; // tipo primitivo short Armazena 32767
		
		float nota = 4.8f;  // tipo primitivo float 
		
		int alunos = 100 , classes  = 20 ;  //tipo primitivo int
		
		long moradores = 9050100 ; // tipo primitivo long
		
		double dolar = 5.47;  // tipo primitivo double
		
		boolean alternativa = false;  // tipo primitivo boolean
		
		// Processamento e Saida de dados 
		
		System.out.println("\nsexo: " + sexo + "\nidade : " + idade + "\ncodigo : " + codigo );
		System.out.println("\nnota : " + nota + "\nalunos : " + alunos + "\nclasses : " + classes);
		System.out.println("\nmoradores: " + moradores + "\ndolar: "+ dolar + "\nalternativa: " + alternativa);

	}

}
